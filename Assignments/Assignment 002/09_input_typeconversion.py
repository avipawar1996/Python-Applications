# Question 09:
# Write a program to take user's name and age and display:
# Hello <name>, You will turn <age + 1> next year.

# Solution:
user_name = input("Enter your name: ")
user_age = int(input("Enter your age: "))

print("Hello " + user_name + ", You will turn " + str(user_age+1) + " next year.")

# Output:
# Enter your name: Samuel
# Enter your age: 28
# Hello Samuel, You will turn 29 next year.