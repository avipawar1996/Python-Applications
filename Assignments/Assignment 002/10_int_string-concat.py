# What will be the output:
# print("10" + "20")
# print(10 + 20)
# Explain the difference

# Solution:
# print("10" + "20") => 1020
# print(10 + 20) => 30

# Program:
print("10" + "20")
print(10 + 20)

# Output:
1020
30

# Explaination: 
# print("10" + "20") here both are str variables and hence get concatenated (append the text) so the output is 1020
# print(10 + 20) here both are int variables and hence arithematic operation of addition is peformed on them so the output is 30.