# Question 06:
# Write a progam to accept two numbers from user and perform:
# Addition
# Substraction
# Multiplication
# Division

# Solution:

num1 = int(input("Enter number 1: "))
num2 = int(input("Enter number 2: "))

print(num1, "+", num2, "=", num1 + num2)
print(num1, "-", num2, "=", num1 - num2)
print(num1, "*", num2, "=", num1 * num2)
print(num1, "/", num2, "=", num1 / num2)

# Solution:
# Enter number 1: 10
# Enter number 2: 3
# 10 + 3 = 13
# 10 - 3 = 7
# 10 * 3 = 30
# 10 / 3 = 3.3333333333333335